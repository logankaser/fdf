#include "wrapper.h"

int			draw(t_ctx *c)
{
	t_list *bfs;

	bfs = c->buffs;
	while (bfs)
	{
		buffer_blit(c, bfs->content, 0, 0);
		bfs = bfs->next;
	}
	return (0);
}
